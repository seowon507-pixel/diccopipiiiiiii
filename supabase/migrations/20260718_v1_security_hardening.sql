-- Revoke legacy RPCs without assuming their exact argument lists.
do $migration$
declare
  fn record;
begin
  for fn in
    select p.oid::regprocedure as signature
    from pg_proc p
    join pg_namespace n on n.oid = p.pronamespace
    where n.nspname = 'public'
      and p.proname in (
        'create_post_with_owner', 'delete_own_post', 'create_pin_with_owner',
        'delete_own_pin', 'delete_expired_pins'
      )
  loop
    execute 'revoke execute on function ' || fn.signature || ' from public, anon, authenticated';
  end loop;
end
$migration$;

