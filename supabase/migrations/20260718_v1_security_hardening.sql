create or replace function public.create_post_v2(
  p_post_id uuid,
  p_actor_token text,
  p_owner_secret text,
  p_author_lat double precision,
  p_author_lng double precision,
  p_lat double precision,
  p_lng double precision,
  p_category text,
  p_title text,
  p_content text,
  p_image_url text default null,
  p_icon text default null
) returns public.posts
language plpgsql
security definer
set search_path = pg_catalog, public, extensions
as $function$
declare
  v_post public.posts;
begin
  perform public.assert_write_rate_v1('post_create', p_actor_token, 5, interval '10 minutes');

  if p_owner_secret is null or char_length(p_owner_secret) < 32 then
    raise exception 'invalid owner secret' using errcode = '22023';
  end if;
  if p_author_lat not between -90 and 90 or p_author_lng not between -180 and 180
     or p_lat not between -90 and 90 or p_lng not between -180 and 180 then
    raise exception 'invalid coordinates' using errcode = '22023';
  end if;
  if p_image_url is not null
     and position('/storage/v1/object/public/post-images/' in p_image_url) = 0 then
    raise exception 'invalid post image URL' using errcode = '22023';
  end if;

  insert into public.posts (
    id, lat, lng, category, title, content, post_type, image_url, icon, created_at
  ) values (
    p_post_id, p_lat, p_lng, p_category, btrim(p_title), btrim(p_content),
    case when public.distance_m_v1(p_author_lat, p_author_lng, p_lat, p_lng) > 500
      then 'external' else 'local' end,
    p_image_url, p_icon, clock_timestamp()
  )
  on conflict (id) do nothing
  returning * into v_post;

  if v_post.id is null then
    if not exists (
      select 1 from public.post_owners po
      where po.post_id = p_post_id
        and po.owner_secret_hash = extensions.digest(p_owner_secret, 'sha256')
    ) then
      raise exception 'idempotency key already used' using errcode = '23505';
    end if;

    select * into strict v_post from public.posts where id = p_post_id;
    return v_post;
  end if;

  insert into public.post_owners(post_id, owner_secret, owner_secret_hash)
  values (p_post_id, null, extensions.digest(p_owner_secret, 'sha256'));

  return v_post;
end;
$function$;

create or replace function public.update_own_post_v2(
  p_post_id uuid,
  p_owner_secret text,
  p_category text,
  p_title text,
  p_content text,
  p_image_url text default null,
  p_icon text default null
) returns public.posts
language plpgsql
security definer
set search_path = pg_catalog, public, extensions
as $function$
declare
  v_post public.posts;
  v_old_image_url text;
  v_old_path text;
begin
  if not exists (
    select 1 from public.post_owners po
    where po.post_id = p_post_id
      and po.owner_secret_hash = extensions.digest(p_owner_secret, 'sha256')
  ) then
    raise exception 'not owner' using errcode = '42501';
  end if;
  if p_image_url is not null
     and position('/storage/v1/object/public/post-images/' in p_image_url) = 0 then
    raise exception 'invalid post image URL' using errcode = '22023';
  end if;

  select image_url into v_old_image_url
  from public.posts where id = p_post_id for update;

  update public.posts
  set category = p_category,
      title = btrim(p_title),
      content = btrim(p_content),
      image_url = p_image_url,
      icon = p_icon,
      updated_at = clock_timestamp()
  where id = p_post_id
  returning * into strict v_post;

  if v_old_image_url is distinct from p_image_url and v_old_image_url is not null then
    v_old_path := public.storage_object_path_v1(v_old_image_url);
    if v_old_path is not null then
      insert into public.storage_cleanup_queue(bucket_id, object_path, reason)
      values ('post-images', v_old_path, 'post_image_replaced')
      on conflict (bucket_id, object_path) do update
        set processed_at = null, available_after = clock_timestamp(), last_error = null;
    end if;
  end if;

  return v_post;
end;
$function$;

