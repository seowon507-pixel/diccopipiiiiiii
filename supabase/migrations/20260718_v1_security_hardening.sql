-- ---------------------------------------------------------------------------
-- Public cleanup enqueue is restricted to unreferenced post-images and delayed
-- one hour. The service-role worker MUST re-check references before Storage API
-- removal to close the upload/attach race.
-- ---------------------------------------------------------------------------

create or replace function public.enqueue_storage_cleanup_v1(
  p_bucket_id text,
  p_object_path text,
  p_reason text default 'abandoned_upload'
) returns boolean
language plpgsql
security definer
set search_path = pg_catalog, public
as $function$
begin
  if p_bucket_id <> 'post-images'
     or p_object_path is null
     or p_object_path = ''
     or p_object_path like '../%'
     or position('/../' in p_object_path) > 0 then
    raise exception 'invalid storage object' using errcode = '22023';
  end if;
  if p_reason <> 'abandoned_upload' then
    raise exception 'invalid cleanup reason' using errcode = '22023';
  end if;
  if exists (
    select 1 from public.posts p
    where public.storage_object_path_v1(p.image_url) = p_object_path
  ) then
    return false;
  end if;

  insert into public.storage_cleanup_queue(
    bucket_id, object_path, reason, available_after
  ) values (
    p_bucket_id, p_object_path, p_reason, clock_timestamp() + interval '1 hour'
  )
  on conflict (bucket_id, object_path) do update
    set processed_at = null,
        reason = excluded.reason,
        available_after = greatest(public.storage_cleanup_queue.available_after, excluded.available_after),
        last_error = null;
  return true;
end;
$function$;

