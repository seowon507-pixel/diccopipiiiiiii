create or replace function public.distance_m_v1(
  p_lat1 double precision,
  p_lng1 double precision,
  p_lat2 double precision,
  p_lng2 double precision
) returns double precision
language sql
immutable
strict
set search_path = pg_catalog
as $function$
  select 6371000 * 2 * asin(sqrt(least(1.0, greatest(0.0,
    power(sin(radians(p_lat2 - p_lat1) / 2), 2) +
    cos(radians(p_lat1)) * cos(radians(p_lat2)) *
    power(sin(radians(p_lng2 - p_lng1) / 2), 2)
  ))));
$function$;

create or replace function public.assert_write_rate_v1(
  p_scope text,
  p_actor_token text,
  p_max_count integer,
  p_window interval
) returns void
language plpgsql
security definer
set search_path = pg_catalog, public, extensions
as $function$
declare
  v_actor_hash bytea;
  v_count integer;
begin
  if p_actor_token is null or char_length(p_actor_token) < 32 then
    raise exception 'invalid actor token' using errcode = '22023';
  end if;
  if p_max_count < 1 or p_window <= interval '0 seconds' then
    raise exception 'invalid rate limit configuration' using errcode = '22023';
  end if;

  v_actor_hash := extensions.digest(p_actor_token, 'sha256');
  perform pg_advisory_xact_lock(hashtextextended(p_scope || encode(v_actor_hash, 'hex'), 0));

  select count(*) into v_count
  from public.write_rate_events e
  where e.scope = p_scope
    and e.actor_hash = v_actor_hash
    and e.created_at >= clock_timestamp() - p_window;

  if v_count >= p_max_count then
    raise exception 'rate limit exceeded'
      using errcode = 'P0001', hint = 'RATE_LIMIT';
  end if;

  insert into public.write_rate_events(scope, actor_hash)
  values (p_scope, v_actor_hash);
end;
$function$;

create or replace function public.storage_object_path_v1(p_image_url text)
returns text
language sql
immutable
set search_path = pg_catalog
as $function$
  select case
    when p_image_url is null or position('/post-images/' in p_image_url) = 0 then null
    else split_part(split_part(p_image_url, '/post-images/', 2), '?', 1)
  end;
$function$;

revoke all on function public.distance_m_v1(double precision,double precision,double precision,double precision) from public, anon, authenticated;
revoke all on function public.assert_write_rate_v1(text,text,integer,interval) from public, anon, authenticated;
revoke all on function public.storage_object_path_v1(text) from public, anon, authenticated;

